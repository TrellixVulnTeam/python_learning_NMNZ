def sum_2_num(num1, num2):  # parameter
    """sum of two number

    :param num1: first number
    :param num2: second number
    :return:
    """
    return num1 + num2  # return


def mi_2_num(num1, num2):  # parameter
    """mi of two number

    :param num1: first number
    :param num2: second number
    :return:
    """
    return num1 - num2  # return
